static const char *frag_denoise_source =
    "#version 120\n"
    "uniform sampler2D screenTex;\n"
    "uniform vec2 uTexelSize;\n"
    "varying vec2 vTexCoord;\n"
    "vec2 getUV(vec2 tc) { return vec2(tc.x, 1.0 - tc.y); }\n"
    "float getLuma(vec3 color) { return dot(color, vec3(0.299, 0.587, 0.114)); }\n"
    "void main() {\n"
    "    vec2 uv = getUV(vTexCoord);\n"
    "    vec4 center = texture2D(screenTex, uv);\n"
    "    float centerLuma = getLuma(center.rgb);\n"
    "    vec4 sum = vec4(0.0);\n"
    "    float totalWeight = 0.0;\n"
    "    float sigmaSpace = 2.0;\n"
    "    float sigmaColor = 0.1;\n"
    "    for(int j = -2; j <= 2; j++) {\n"
    "        for(int i = -2; i <= 2; i++) {\n"
    "            vec2 offset = vec2(float(i), float(j));\n"
    "            vec2 sampleUv = uv + offset * uTexelSize;\n"
    "            vec4 sample = texture2D(screenTex, sampleUv);\n"
    "            float sampleLuma = getLuma(sample.rgb);\n"
    "            float spaceDist = length(offset);\n"
    "            float colorDist = abs(centerLuma - sampleLuma);\n"
    "            float wSpace = exp(-(spaceDist * spaceDist) / (2.0 * sigmaSpace * sigmaSpace));\n"
    "            float wColor = exp(-(colorDist * colorDist) / (2.0 * sigmaColor * sigmaColor));\n"
    "            float weight = wSpace * wColor;\n"
    "            sum += sample * weight;\n"
    "            totalWeight += weight;\n"
    "        }\n"
    "    }\n"
    "    gl_FragColor = sum / totalWeight;\n"
    "}\n";
// --- ШЕЙДЕР ПОСТ-ОБРОБКИ ТА RCAS (Pass 2) ---
static const char *post_fragment_source =
    "#version 120\n"
    "uniform sampler2D screenTex;\n"
    "uniform vec2 uTexelSize;\n"
    "uniform float uSharpness;\n"
    "uniform float uContrast;\n"
    "uniform float uBrightness;\n"
    "uniform float uGammaValue;\n"
    "uniform int uTextMode;\n"
    "uniform float uZoomLevel;\n"
    "varying vec2 vTexCoord;\n"
    "vec4 sharpenAdaptive(vec4 centerColor, vec2 uv) {\n"
    "    vec4 l = texture2D(screenTex, uv + vec2(-uTexelSize.x, 0.0));\n"
    "    vec4 r = texture2D(screenTex, uv + vec2(uTexelSize.x, 0.0));\n"
    "    vec4 u = texture2D(screenTex, uv + vec2(0.0, -uTexelSize.y));\n"
    "    vec4 d = texture2D(screenTex, uv + vec2(0.0, uTexelSize.y));\n"
    "    vec4 blur = (l + r + u + d) * 0.25;\n"
    "    vec4 minN = min(centerColor, min(min(l, r), min(u, d)));\n"
    "    vec4 maxN = max(centerColor, max(max(l, r), max(u, d)));\n"
    "    vec4 edgeDiff = maxN - minN;\n"
    "    float maxEdge = max(max(edgeDiff.r, edgeDiff.g), edgeDiff.b);\n"
    "    float adaptiveSharp = uSharpness * clamp(1.0 - maxEdge, 0.0, 1.0);\n"
    "    return centerColor + (centerColor - blur) * adaptiveSharp;\n"
    "}\n"
    "void main() {\n"
    "    vec2 uv = vTexCoord;\n"
    "    vec4 color = texture2D(screenTex, uv);\n"
    "    vec4 left  = texture2D(screenTex, uv + vec2(-uTexelSize.x, 0.0));\n"
    "    vec4 right = texture2D(screenTex, uv + vec2(uTexelSize.x, 0.0));\n"
    "    vec4 up    = texture2D(screenTex, uv + vec2(0.0, -uTexelSize.y));\n"
    "    vec4 down  = texture2D(screenTex, uv + vec2(0.0, uTexelSize.y));\n"
    "    vec4 blur = (left + right + up + down) * 0.25;\n"
    "    float zoomSharpness = uZoomLevel > 1.2 ? uSharpness : uSharpness * 0.35;\n"
    "    if (uSharpness > 0.0) color = color + (color - blur) * zoomSharpness;\n"
    "    if (uTextMode == 1) {\n"
    "        float luma = dot(color.rgb, vec3(0.299, 0.587, 0.114));\n"
    "        vec3 gray = vec3(luma);\n"
    "        color.rgb = mix(color.rgb, gray, 0.20);\n"
    "        vec4 textEdge = clamp((color - blur) * 2.5, -0.15, 0.15);\n"
    "        color.rgb += textEdge.rgb;\n"
    "        color.rgb = (color.rgb - 0.5) * 1.15 + 0.5;\n"
    "    }\n"
    "    color.rgb = (color.rgb - 0.5) * uContrast + 0.5 + uBrightness;\n"
    "    color.rgb = pow(clamp(color.rgb, 0.0, 1.0), vec3(1.0 / uGammaValue));\n"
    "    gl_FragColor = vec4(clamp(color.rgb, 0.0, 1.0), color.a);\n"
    "}\n";
