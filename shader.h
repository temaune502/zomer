static const char *frag_fsr_source =
    "#version 120\n"
    "uniform sampler2D screenTex;\n"
    "uniform vec2 uTexelSize;\n"
    "varying vec2 vTexCoord;\n"
    "vec2 getUV(vec2 tc) { return vec2(tc.x, 1.0 - tc.y); }\n"
    "float getLuma(vec3 c) { return dot(c, vec3(0.299, 0.587, 0.114)); }\n"
    "void main() {\n"
    "    vec2 uv = getUV(vTexCoord);\n"
    "    vec2 pos = uv / uTexelSize - 0.5;\n"
    "    vec2 iPos = floor(pos);\n"
    "    vec2 fPos = fract(pos);\n"
    "\n"
    "    // 1. Зчитуємо критичний мінімум - квадрат 2x2 навколо точки\n"
    "    vec4 p00 = texture2D(screenTex, (iPos + vec2(0.5, 0.5)) * uTexelSize);\n"
    "    vec4 p10 = texture2D(screenTex, (iPos + vec2(1.5, 0.5)) * uTexelSize);\n"
    "    vec4 p01 = texture2D(screenTex, (iPos + vec2(0.5, 1.5)) * uTexelSize);\n"
    "    vec4 p11 = texture2D(screenTex, (iPos + vec2(1.5, 1.5)) * uTexelSize);\n"
    "\n"
    "    // Обчислюємо локальні яскравості\n"
    "    float l00 = getLuma(p00.rgb); float l10 = getLuma(p10.rgb);\n"
    "    float l01 = getLuma(p01.rgb); float l11 = getLuma(p11.rgb);\n"
    "\n"
    "    // Знаходимо локальний контраст у квадраті 2x2\n"
    "    float minLuma = min(min(l00, l10), min(l01, l11));\n"
    "    float maxLuma = max(max(l00, l10), max(l01, l11));\n"
    "    float localContrast = maxLuma - minLuma;\n"
    "\n"
    "    // 2. ДИНАМІЧНА КОРЕКЦІЯ КООРДИНА (Захист дрібних деталей)\n"
    "    // Якщо контраст дуже високий (дрібний текст/іконка, ми робимо криву переходу\n"
    "    // надзвичайно крутою за допомогою smoothstep. Це не дає пікселям «повзти» вбік.\n"
    "    vec2 fPosAdjusted = fPos;\n"
    "    if (localContrast > 0.15) {\n"
    "        float strength = smoothstep(0.15, 0.6, localContrast);\n"
    "        // Надважлива математика: підтискаємо інтерполяцію до центрів оригінальних пікселів\n"
    "        fPosAdjusted.x = mix(fPos.x, smoothstep(0.0, 1.0, fPos.x), strength);\n"
    "        fPosAdjusted.y = mix(fPos.y, smoothstep(0.0, 1.0, fPos.y), strength);\n"
    "    }\n"
    "\n"
    "    // 3. Виконуємо високоточну кубічну інтерполяцію (Bicubic Hermite) за скоригованими координатами\n"
    "    vec2 w0 = fPosAdjusted * fPosAdjusted * (3.0 - 2.0 * fPosAdjusted);\n"
    "    \n"
    "    vec4 mixX1 = mix(p00, p10, w0.x);\n"
    "    vec4 mixX2 = mix(p01, p11, w0.x);\n"
    "    vec4 finalColor = mix(mixX1, mixX2, w0.y);\n"
    "\n"
    "    // 4. ЗАЛІЗОБЕТОННЕ ОБМЕЖЕННЯ (Anti-Bleeding)\n"
    "    // Повністю виключає появу сірого туману чи сторонніх кольорів навколо літер\n"
    "    vec4 minColor = min(min(p00, p10), min(p01, p11));\n"
    "    vec4 maxColor = max(max(p00, p10), max(p01, p11));\n"
    "    \n"
    "    gl_FragColor = clamp(finalColor, minColor, maxColor);\n"
    "}\n";
