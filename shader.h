    "void main() {\n"
    "    vec2 uv = vTexCoord;\n"
    "    vec4 c = texture2D(screenTex, uv);\n"
    "    vec4 n = texture2D(screenTex, uv + vec2(0.0, -uTexelSize.y));\n"
    "    vec4 s = texture2D(screenTex, uv + vec2(0.0, uTexelSize.y));\n"
    "    vec4 e = texture2D(screenTex, uv + vec2(uTexelSize.x, 0.0));\n"
    "    vec4 w = texture2D(screenTex, uv + vec2(-uTexelSize.x, 0.0));\n"
    "    vec4 minN = min(c, min(min(n, s), min(e, w)));\n"
    "    vec4 maxN = max(c, max(max(n, s), max(e, w)));\n"
    "    float maxEdge = max(max(maxN.r - minN.r, maxN.g - minN.g), maxN.b - minN.b);\n"
    "    float sharpStrength = clamp(maxEdge * 2.0, 0.0, 0.5);\n"
    "    vec4 avg = (n + s + e + w) * 0.25;\n"
    "    vec4 color = c + (c - avg) * sharpStrength;\n"
    "    if (uTextMode == 1) {\n"
    "        float luma = dot(color.rgb, vec3(0.299, 0.587, 0.114));\n"
    "        vec3 gray = vec3(luma);\n"
    "        color.rgb = mix(color.rgb, gray, 0.20);\n"
    "        vec4 textEdge = clamp((color - avg) * 2.5, -0.15, 0.15);\n"
    "        color.rgb += textEdge.rgb;\n"
    "        color.rgb = (color.rgb - 0.5) * 1.15 + 0.5;\n"
    "    }\n"
    "    gl_FragColor = color;\n"
    "}\n";
