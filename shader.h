// --- ШЕЙДЕР CATMULL-ROM (Режим 4) ---
static const char *frag_catmull_source =
    "#version 120\n"
    "uniform sampler2D screenTex;\n"
    "uniform vec2 uTexelSize;\n"
    "varying vec2 vTexCoord;\n"
    "vec2 getUV(vec2 tc) { return vec2(tc.x, 1.0 - tc.y); }\n"
    "vec4 catmullRomWeights(float x) {\n"
    "    float x2 = x * x; float x3 = x2 * x;\n"
    "    return vec4(\n"
    "        -0.5 * x3 + 1.0 * x2 - 0.5 * x,\n"
    "         1.5 * x3 - 2.5 * x2 + 1.0,\n"
    "        -1.5 * x3 + 2.0 * x2 + 0.5 * x,\n"
    "         0.5 * x3 - 0.5 * x2\n"
    "    );\n"
    "}\n"
    "void main() {\n"
    "    vec2 uv = getUV(vTexCoord);\n"
    "    vec2 size = 1.0 / uTexelSize;\n"
    "    vec2 pos = uv * size - 0.5;\n"
    "    vec2 iPos = floor(pos);\n"
    "    vec2 fPos = fract(pos);\n"
    "    vec4 wX = catmullRomWeights(fPos.x);\n"
    "    vec4 wY = catmullRomWeights(fPos.y);\n"
    "    vec4 centerValue = vec4(0.0);\n"
    "    vec4 minColor = vec4(1.0);\n"
    "    vec4 maxColor = vec4(0.0);\n"
    "    for(int j = -1; j <= 2; j++) {\n"
    "        float wy = wY[j+1];\n"
    "        for(int i = -1; i <= 2; i++) {\n"
    "            float wx = wX[i+1];\n"
    "            vec2 sample_uv = (iPos + vec2(float(i), float(j)) + 0.5) * uTexelSize;\n"
    "            vec4 sample = texture2D(screenTex, sample_uv);\n"
    "            centerValue += sample * (wx * wy);\n"
    "            minColor = min(minColor, sample);\n"
    "            maxColor = max(maxColor, sample);\n"
    "        }\n"
    "    }\n"
    "    gl_FragColor = clamp(centerValue, minColor, maxColor);\n"
    "}\n";

